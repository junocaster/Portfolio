    private void clearTablesAndFields() {
        ((DefaultTableModel) incomeTable.getModel()).setRowCount(0);
        ((DefaultTableModel) expenseTable.getModel()).setRowCount(0);
        income.setText("");
        expense.setText("");
        netBalance.setText("");
    }
    
    
    private double processTransactions(String fileName, Date startDate, Date endDate, TableModel model) {
       double totalAmount = 0.0;
        try (BufferedReader reader = new BufferedReader(new FileReader(fileName))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 3) {
                    String dateText = parts[0].trim();
                    Date transactionDate = new SimpleDateFormat("MM-dd-yyyy").parse(dateText);

                    // Check if the transaction date is within the selected range
                    if ((startDate == null || !transactionDate.before(startDate)) &&
                            (endDate == null || !transactionDate.after(endDate))) {

                        String category = parts[1].trim();
                        double amount = Double.parseDouble(parts[2].trim());

                        // Add the transaction to the table
                        addTransactionToTable(category, amount, model);

                        // Update total amount
                        totalAmount += amount;
                    }
                }
            }
        } catch (IOException | ParseException e) {
            e.printStackTrace();
        }
        return totalAmount;
    }

    private void addTransactionToTable(String category, double amount, TableModel model) {
    for (int row = 0; row < model.getRowCount(); row++) {
        String tableCategory = model.getValueAt(row, 0).toString();
        if (tableCategory.equals(category)) {
            double currentAmount = Double.parseDouble(model.getValueAt(row, 1).toString());
            double updatedAmount = currentAmount + amount;
                 
            model.setValueAt(String.format("%.2f", updatedAmount), row, 1);
            return;
        }
    }
    
    ((DefaultTableModel) model).addRow(new Object[]{category, String.format("%.2f", amount)});
    }    
