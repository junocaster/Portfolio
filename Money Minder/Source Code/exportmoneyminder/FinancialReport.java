    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
    clearTablesAndFields();

    // Retrieve values from date pickers
    Date startDate = (Date) fromDate.getDate();
    Date endDate = (Date) toDate.getDate();
    
     if (startDate == null || endDate == null) {
        JOptionPane.showMessageDialog(this, "Please select both start and end dates.", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }

    double totalIncome = processTransactions("income.txt", startDate, endDate, incomeTable.getModel());
    double totalExpense = processTransactions("expenses.txt", startDate, endDate, expenseTable.getModel());
    double totalNetBalance = totalIncome - totalExpense;

    income.setText(String.format("P %.2f",totalIncome));
    expense.setText(String.format("P %.2f", totalExpense));
    netBalance.setText(String.format("P %.2f", totalNetBalance));
    }//GEN-LAST:event_enterActionPerformed

