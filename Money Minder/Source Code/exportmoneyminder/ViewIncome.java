    private void loadTransactionsFromFile() {
        try (BufferedReader reader = new BufferedReader(new FileReader("income.txt"))) {
            DefaultTableModel model = (DefaultTableModel) incomeTable.getModel();

            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 3) {
                    String dateText = parts[0].trim();
                    String category = parts[1].trim();
                    double amount = Double.parseDouble(parts[2].trim());

                    // Create a Transaction object
                    Transaction transaction = new Transaction(dateText, category, amount);

                    // Add the transaction to the table
                    addTransactionToTable(transaction, model);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void addTransactionToTable(Transaction transaction, DefaultTableModel model) {
        model.addRow(new Object[]{transaction.getDate(), transaction.getCategory(), String.format("P %.2f", transaction.getAmount())});
    }


    private void saveTransactionsToFile() {
        try (FileWriter writer = new FileWriter("income.txt")) {
            DefaultTableModel model = (DefaultTableModel) incomeTable.getModel();
            for (int row = 0; row < model.getRowCount(); row++) {
                String date = model.getValueAt(row, 0).toString();
                String category = model.getValueAt(row, 1).toString();
                String amount = model.getValueAt(row, 2).toString().replace("P ", "");

                // Create a Transaction object
                Transaction transaction = new Transaction(date, category, Double.parseDouble(amount));

                writer.write(transaction.getDate() + ", " + transaction.getCategory() + ", " + transaction.getAmount());
                writer.write(System.lineSeparator());
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void filterTransactions(String fileName, Date startDate, Date endDate, DefaultTableModel model) {
        try (BufferedReader reader = new BufferedReader(new FileReader(fileName))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 3) {
                    String dateText = parts[0].trim();
                    Date transactionDate = new SimpleDateFormat("MM-dd-yyyy").parse(dateText);

                    // Create a Transaction object
                    Transaction transaction = new Transaction(dateText, parts[1].trim(), Double.parseDouble(parts[2].trim()));

                    // Check if the transaction date is within the selected range
                    if ((startDate == null || !transactionDate.before(startDate)) &&
                            (endDate == null || !transactionDate.after(endDate))) {

                        // Add the transaction to the table
                        addTransactionToTable(transaction, model);
                    }
                }
            }
        } catch (IOException | ParseException e) {
            e.printStackTrace();
        }
    }
    
    private void clearTablesAndFields() {
        ((DefaultTableModel) incomeTable.getModel()).setRowCount(0);
    }    
    private boolean isValidDateFormat(String date) {
    SimpleDateFormat sdf = new SimpleDateFormat("MM-dd-yyyy");
    sdf.setLenient(false);

    try {
        sdf.parse(date);
        return true;
    } catch (ParseException e) {
        return false;
    }
}

